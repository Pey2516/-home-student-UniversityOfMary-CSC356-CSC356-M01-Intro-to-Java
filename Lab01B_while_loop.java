
public class Lab01B_while_loop
{
    public static void main( String[] args )
    {
        int lp = 15; // Initialize lp to 5.
        while ( lp >= 5 ) // Prints the the value of lp up to 15.
        {
            System.out.print( lp + " " );
            lp--; // Increase the lp value.
        }
        System.out.print( "\n" );
    }
}