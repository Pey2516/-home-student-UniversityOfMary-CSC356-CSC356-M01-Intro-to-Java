
public class Lab01B_while_loop
{
    public static void main( String[] args )
    {
        int lp = 5; // Initialize lp to 1
        while ( lp <= 15 ) // Execute loop while lp less than or equal to 10
        {
            System.out.print( lp + " " );
            lp++; // Increase the lp value
        }
        System.out.print( "\n" );
    }
}